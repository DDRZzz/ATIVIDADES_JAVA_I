/*7. Crie um programa que imprima 11 vezes a frase " Hello World!" utilizando uma
estrutura sequencial e uma estrutura de repetição while. */

public class Q7 {
    public static void main (String[] args){
        
            int i = 0;
            while (i < 11){
                System.out.println("Hello World");
                i++;
            }
        }
}
